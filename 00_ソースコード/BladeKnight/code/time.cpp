//=======================================
// 初期化
//=======================================
void CTIME::Init()
{
	m_fDeltaTime = 0.0f;
}

//=======================================
// 更新
//=======================================
void CTIME::Update()
{
	// 現在の時間
	DWORD NowTime = timeGetTime();

	m_fDeltaTime = (float)NowTime;
}
